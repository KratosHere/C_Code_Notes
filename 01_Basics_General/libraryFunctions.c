/*

%d => For Integers!
%f => For Real Values!
%c => For Characters!


*/

#include <stdio.h>

int main() {

    int PrintInteger = 23;
    float PrintReal = 42.14;
    char PrintChar = 'c';

    printf("Integer: %d \n" , PrintInteger);

    printf("Real: %f \n" , PrintReal);
    
    printf("Character: %c \n" , PrintChar);

    return 0;

}